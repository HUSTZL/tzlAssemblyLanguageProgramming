.386
DATA SEGMENT USE16
BUF		DB	6 DUP(10H)
N		EQU	($-BUF)
BUF1	DB	N DUP(?)
BUF2	DB	N DUP(?)
DATA ENDS

STACK SEGMENT USE16 STACK
		DW	100 DUP(0)
STACK ENDS

CODE SEGMENT USE16
	ASSUME CS:CODE, SS:STACK, DS:DATA, ES:DATA
START:	MOV AX, DATA
		MOV DS, AX
		MOV ES, AX
		MOV SI, OFFSET BUF;MOV SI, OFFSET BUF2
		MOV DI, OFFSET BUF2;MOV DI, OFFSET BUF
		MOV CX, N
		CLD
		REP MOVSB
		LEA SI, BUF
		LEA DI, BUF1
		MOV CX, N
LOOPA:	MOV AL, BYTE PTR [SI]
		MOV BYTE PTR DS:[DI], AL
		INC SI
		INC DI
		LOOP LOOPA
		MOV AH, 4CH
		INT 21H
CODE ENDS
	END START